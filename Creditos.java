
package gui_bd;

 public class Creditos {
    
     private String Fecha;
     private float CreditoInicial;
     private float CreditoFinal;

    public String getFecha() {
        return Fecha;
    }

    public void setFecha(String Fecha) {
        this.Fecha = Fecha;
    }

    public float getCreditoInicial() {
        return CreditoInicial;
    }

    public void setCreditoInicial(float CreditoInicial) {
        this.CreditoInicial = CreditoInicial;
    }

    public float getCreditoFinal() {
        return CreditoFinal;
    }

    public void setCreditoFinal(float CreditoFinal) {
        this.CreditoFinal = CreditoFinal;
    }
     
     
}
